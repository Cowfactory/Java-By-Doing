public class MyInitials {
	public static void main(String[] args) {
		System.out.println("For my name, my initials are... \n");
		System.out.println("H   H   SSS   H    H");
		System.out.println("H   H  S   S  H    H");
		System.out.println("H   H  S      H    H");
		System.out.println("HHHHH   SSS   HHHHHH");
		System.out.println("H   H      S  H    H");
		System.out.println("H   H  S   S  H    H");
		System.out.println("H   H   SSS   H    H");
	}
}